// Get all professions as a flat list
export const getAllProfessions = (lang = 'en') => {
  const professions = [];
  Object.values(QUEBEC_PROFESSIONS[lang]).forEach(category => {
    category.professions.forEach(prof => {
      professions.push({
        name: prof,
        category: category.label,
        icon: category.icon
      });
    });
  });
  return professions;
};

// Get categories list
export const getCategories = (lang = 'en') => {
  return Object.entries(QUEBEC_PROFESSIONS[lang]).map(([key, value]) => ({
    id: key,
    label: value.label,
    icon: value.icon,
    count: value.professions.length
  }));
};

// Search professions
export const searchProfessions = (query, lang = 'en') => {
  const results = [];
  const lowerQuery = query.toLowerCase();

  Object.values(QUEBEC_PROFESSIONS[lang]).forEach(category => {
    category.professions.forEach(prof => {
      if (prof.toLowerCase().includes(lowerQuery)) {
        results.push({
          name: prof,
          category: category.label,
          icon: category.icon
        });
      }
    });
  });

  return results;
};
